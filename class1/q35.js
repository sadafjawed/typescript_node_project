var pet_animals = ["dog", "cat", "rabbit"];
for (var i = 0; i < pet_animals.length; i++) {
    if (pet_animals[i] == "dog") {
        console.log(pet_animals[i] + " is good for protection");
    }
    else if (pet_animals[i] == "cat") {
        console.log(pet_animals[i] + " is good for play");
    }
    else if (pet_animals[i] == "rabbit") {
        console.log(pet_animals[i] + " is good for health");
    }
}
